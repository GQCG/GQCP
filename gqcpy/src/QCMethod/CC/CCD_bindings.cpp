#include "Mathematical/Algorithm/IterativeAlgorithm.hpp"
#include "QCMethod/CC/CCD.hpp"
#include "QCMethod/CC/CCDSolver.hpp"
#include "Utilities/complex.hpp"

#include <pybind11/pybind11.h>


namespace gqcpy {


// Provide some shortcuts for frequent namespaces.
namespace py = pybind11;
using namespace GQCP;


/**
 *  Add Python bindings for some APIs related to `QCMethod::CCD`.
 * 
 *  @tparam Class               The type of the Pybind11 `class_` (generated by the compiler).
 *  
 *  @param py_class             The Pybind11 `class_` that should obtain APIs related to `QCMethod::CCD`.
 */
template <typename Class>
void bindQCMethodCCDInterface(Class& py_class) {

    // The C++ type corresponding to the Python class.
    using Type = typename Class::type;

    using Scalar = typename Type::Scalar;


    py_class
        .def_static(
            "optimize",
            [](IterativeAlgorithm<CCSDEnvironment<Scalar>>& solver, CCSDEnvironment<Scalar>& environment) {
                return Type().optimize(solver, environment);
            },
            py::arg("solver"),
            py::arg("environment"),
            "Optimize the CCD wave function model.");
}


/**
 *  Register multiple templated `QCMethod::CCD` to the gqcpy module and expose parts of their C++ interfaces to Python.
 * 
 *  @param module           The Pybind11 module in which the classes should be registered.
 */
void bindQCMethodCCD(py::module& module) {

    // Real-valued Python bindings.
    py::class_<QCMethod::CCD<double>> py_CCD_d {module, "CCD_d", "The (real-valued) CCD quantum chemical method."};
    bindQCMethodCCDInterface(py_CCD_d);

    // Complex-valued Python bindings.
    py::class_<QCMethod::CCD<complex>> py_CCD_cd {module, "CCD_cd", "The (complex-valued) CCD quantum chemical method."};
    bindQCMethodCCDInterface(py_CCD_cd);
}


}  // namespace gqcpy
