#include "Basis/BiorthogonalBasis/RLowdinPairingBasis.hpp"
#include "gqcpy/include/interfaces.hpp"

#include <pybind11/pybind11.h>
#include <pybind11/stl.h>


namespace py = pybind11;


namespace gqcpy {


// Provide some shortcuts for frequent namespaces.
namespace py = pybind11;
using namespace GQCP;


/**
 *  Add Python bindings for some APIs related to `RLowdinPairingBasis`.
 *
 *  @tparam Class               The type of the Pybind11 `class_` (generated by the compiler).
 *
 *  @param py_class             The Pybind11 `class_` that should obtain APIs related to `RLowdinPairingBasis`.
 */
template <typename Class>
void bindRLowdinPairingBasisInterface(Class& py_class) {

    // The C++ type corresponding to the Python class.
    using Type = typename Class::type;
    using Scalar = typename Type::Scalar;


    /**
     *  MARK: Constructors
     */

    py_class
        .def(py::init<const RTransformation<Scalar>&, const RTransformation<Scalar>&, const ScalarRSQOneElectronOperator<Scalar>&, const size_t, const double>(),
             py::arg("C_bra"),
             py::arg("C_ket"),
             py::arg("overlap_operator"),
             py::arg("number_of_occupied_orbitals"),
             py::arg("zero_threshold") = 1e-8);


    // Expose some Mulliken API to the Python class;
    bindLowdinPairingBasisInterface(py_class);
}


/**
 *  Register `RLowdinPairingBasis_d` and `RLowdinPairingBasis_cd` to the gqcpy module and expose parts of their C++ interfaces to Python.
 *
 *  @param module           The Pybind11 module in which the classes should be registered.
 */
void bindRLowdinPairingBases(py::module& module) {

    // Define the Python class for `RLowdinPairingBasis_d`.
    py::class_<RLowdinPairingBasis<double>> py_RLowdinPairingBasis_d {module, "RLowdinPairingBasis_d", "A class that represents a real, biorthogonalized Lowdin pairing basis, created from `restricted` states."};

    bindRLowdinPairingBasisInterface(py_RLowdinPairingBasis_d);


    // Define the Python class for `RLowdinPairingBasis_cd`.
    py::class_<RLowdinPairingBasis<complex>> py_RLowdinPairingBasis_cd {module, "RLowdinPairingBasis_cd", "A class that represents a complex, biorthogonalized Lowdin pairing basis, created from `restricted` states."};

    bindRLowdinPairingBasisInterface(py_RLowdinPairingBasis_cd);
}


}  // namespace gqcpy
